package com.hiusers.iao.api.http.router.type

/**
 * 请求类型枚举
 */
enum class RequestType {

    /**
     * GET 请求
     */
    GET,

    /**
     * POST 请求
     */
    POST,

    /**
     * PUT 请求
     */
    PUT,

    /**
     * PATCH 请求
     */
    PATCH,

    /**
     * DELETE 请求
     */
    DELETE,

    /**
     * HEAD 请求
     */
    HEAD,

    /**
     * OPTIONS 请求
     */
    OPTIONS

}
